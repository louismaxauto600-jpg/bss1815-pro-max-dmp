document.addEventListener("DOMContentLoaded", () => {
  initializeLanguageSystem();
});

function initializeLanguageSystem() {
  const savedLanguage = getSavedLanguage();

  applyLanguage(savedLanguage);
  initializeLanguageButtons();
  initializeLanguageSelects();
}

function getSavedLanguage() {
  const savedLanguage = localStorage.getItem(
    PRO_MAX_LANGUAGE.storageKey
  );

  if (
    savedLanguage &&
    PRO_MAX_LANGUAGE.supportedLanguages.includes(savedLanguage)
  ) {
    return savedLanguage;
  }

  return PRO_MAX_LANGUAGE.defaultLanguage;
}

function setLanguage(languageCode) {
  if (
    !PRO_MAX_LANGUAGE.supportedLanguages.includes(languageCode)
  ) {
    console.warn(`Unsupported language: ${languageCode}`);
    return;
  }

  localStorage.setItem(
    PRO_MAX_LANGUAGE.storageKey,
    languageCode
  );

  applyLanguage(languageCode);
  dispatchLanguageChange(languageCode);

  if (typeof window.showApplicationMessage === "function") {
    window.showApplicationMessage(
      translate("language_changed", languageCode),
      "success"
    );
  }
}

function applyLanguage(languageCode) {
  const selectedLanguage =
    PRO_MAX_LANGUAGE.supportedLanguages.includes(languageCode)
      ? languageCode
      : PRO_MAX_LANGUAGE.defaultLanguage;

  document.documentElement.lang = selectedLanguage;
  document.documentElement.dataset.language = selectedLanguage;

  translateTextElements(selectedLanguage);
  translatePlaceholderElements(selectedLanguage);
  translateTitleElements(selectedLanguage);
  translateAriaLabelElements(selectedLanguage);
  updateLanguageControls(selectedLanguage);
}

function translateTextElements(languageCode) {
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const translationKey = element.dataset.i18n;
    const translatedText = translate(
      translationKey,
      languageCode
    );

    if (translatedText !== null) {
      element.textContent = translatedText;
    }
  });
}

function translatePlaceholderElements(languageCode) {
  document
    .querySelectorAll("[data-i18n-placeholder]")
    .forEach((element) => {
      const translationKey =
        element.dataset.i18nPlaceholder;

      const translatedText = translate(
        translationKey,
        languageCode
      );

      if (translatedText !== null) {
        element.setAttribute("placeholder", translatedText);
      }
    });
}

function translateTitleElements(languageCode) {
  document.querySelectorAll("[data-i18n-title]").forEach((element) => {
    const translationKey = element.dataset.i18nTitle;
    const translatedText = translate(
      translationKey,
      languageCode
    );

    if (translatedText !== null) {
      element.setAttribute("title", translatedText);
    }
  });
}

function translateAriaLabelElements(languageCode) {
  document
    .querySelectorAll("[data-i18n-aria-label]")
    .forEach((element) => {
      const translationKey =
        element.dataset.i18nAriaLabel;

      const translatedText = translate(
        translationKey,
        languageCode
      );

      if (translatedText !== null) {
        element.setAttribute(
          "aria-label",
          translatedText
        );
      }
    });
}

function translate(
  translationKey,
  languageCode = getSavedLanguage()
) {
  const languageDictionary =
    PRO_MAX_LANGUAGE.translations[languageCode];

  const defaultDictionary =
    PRO_MAX_LANGUAGE.translations[
      PRO_MAX_LANGUAGE.defaultLanguage
    ];

  if (
    languageDictionary &&
    Object.prototype.hasOwnProperty.call(
      languageDictionary,
      translationKey
    )
  ) {
    return languageDictionary[translationKey];
  }

  if (
    defaultDictionary &&
    Object.prototype.hasOwnProperty.call(
      defaultDictionary,
      translationKey
    )
  ) {
    return defaultDictionary[translationKey];
  }

  console.warn(
    `Missing translation key: ${translationKey}`
  );

  return null;
}

function initializeLanguageButtons() {
  document
    .querySelectorAll("[data-language]")
    .forEach((button) => {
      button.addEventListener("click", () => {
        const selectedLanguage =
          button.dataset.language;

        setLanguage(selectedLanguage);
      });
    });
}

function initializeLanguageSelects() {
  document
    .querySelectorAll("[data-language-select]")
    .forEach((select) => {
      select.value = getSavedLanguage();

      select.addEventListener("change", () => {
        setLanguage(select.value);
      });
    });
}

function updateLanguageControls(languageCode) {
  document
    .querySelectorAll("[data-language]")
    .forEach((button) => {
      const isActive =
        button.dataset.language === languageCode;

      button.classList.toggle("active", isActive);
      button.setAttribute(
        "aria-pressed",
        String(isActive)
      );
    });

  document
    .querySelectorAll("[data-language-select]")
    .forEach((select) => {
      select.value = languageCode;
    });

  document
    .querySelectorAll("[data-current-language]")
    .forEach((element) => {
      element.textContent =
        PRO_MAX_LANGUAGE.languageNames[languageCode] ||
        languageCode.toUpperCase();
    });
}

function dispatchLanguageChange(languageCode) {
  const languageChangeEvent = new CustomEvent(
    "proMaxLanguageChanged",
    {
      detail: {
        language: languageCode,
        name:
          PRO_MAX_LANGUAGE.languageNames[
            languageCode
          ]
      }
    }
  );

  document.dispatchEvent(languageChangeEvent);
}

function getCurrentLanguage() {
  return getSavedLanguage();
}

window.PRO_MAX_LANGUAGE = PRO_MAX_LANGUAGE;
window.setLanguage = setLanguage;
window.applyLanguage = applyLanguage;
window.translate = translate;
window.getCurrentLanguage = getCurrentLanguage;
